            <p style="text-align: justify;">Salah satu elemen penting dalam mewujudkan penyelenggaraan negara yang terbuka adalah hak publik untuk memperoleh Informasi sesuai dengan peraturan perundang¬undangan. Hak atas Informasi menjadi sangat penting karena makin terbuka penyelenggaraan negara untuk diawasi publik, penyelenggaraan negara tersebut makin dapat dipertanggungjawabkan. Hak setiap Orang untuk memperoleh Informasi juga relevan untuk meningkatkan kualitas pelibatan masyarakat dalam proses pengambilan keputusan publik. Partisipasi atau pelibatan masyarakat tidak banyak berarti tanpa jaminan keterbukaan Informasi Publik.</p>
            <p style="text-align: justify;">Keberadaan Undang-¬undang tentang Keterbukaan Informasi Publik sangat penting sebagai landasan hukum yang berkaitan dengan (1) hak setiap Orang untuk memperoleh Informasi; (2) kewajiban Badan Publik menyediakan dan melayani permintaan Informasi secara cepat, tepat waktu, biaya ringan/proporsional, dan cara sederhana; (3) pengecualian bersifat ketat dan terbatas; (4) kewajiban Badan Publik untuk membenahi sistem dokumentasi dan pelayanan Informasi.</p>
            <p style="text-align: justify;">Setiap Badan Publik mempunyai kewajiban untuk membuka akses atas Informasi Publik yang berkaitan dengan Badan Publik tersebut untuk masyarakat luas. Lingkup Badan Publik dalam Undang-undang ini meliputi lembaga eksekutif, yudikatif, legislatif, serta penyelenggara negara lainnya yang mendapatkan dana dari Anggaran Pendapatan dan Belanja Negara (APBN)/Anggaran Pendapatan dan Belanja Daerah (APBD) dan mencakup pula organisasi nonpemerintah, baik yang berbadan hukum maupun yang tidak berbadan hukum, seperti lembaga swadaya masyarakat, perkumpulan, serta organisasi lainnya yang mengelola atau menggunakan dana yang sebagian atau seluruhnya bersumber dari APBN/APBD, sumbangan masyarakat, dan/atau luar negeri.</p>
            <p style="text-align: justify;">Sesuai dengan amanat pasal 13 UU No.14 Tahun 2008, Kementerian Komunikasi sebagai salah satu Badan Publik telah membentuk Pejabat Pengelola Informasi dan Dokumentasi (PPID) melalui Keputusan Menteri Komunikasi dan Informatika Nomor 117 Tahun 2010 Tentang Organisasi Pengelola Informasi dan Dokumentasi. Dengan terbentuknya PPID pemohon informasi sesuai dengan haknya dapat memperoleh informasi public yang dihasilkan oleh Kementerian Komunikasi dan Informatika sesuai dengan ketentuan dalam UU No. 14 Tahun 2008. Berdasarkan PM Kominfo No.1 Tahun 2016 Tentang Organisasi dan Tata Kerja Kementerian Komunikasi dan Informatika. Maka KM Kominfo No.117 Tahun 2010, dirubah menjadi KM Kominfo No. 1740 Tahun 2016 Tentang Organisasi Pengelola Informasi dan Dokumentasi.</p>
