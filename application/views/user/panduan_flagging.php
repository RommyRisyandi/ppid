             <h2 class="text-primary text-center mb-3">Panduan Flagging Pada Twitter, Facebook dan Youtube</h2>
           <p style="text-align: justify;">(Jakarta, 16 April 2015) – Sebagaimana diketahui bahwa social media telah tumbuh begitu cepat yang tentu memberi banyak manfaat bagi masyarakat terutama untuk mendukung kegiatan-kegiatan yang positif. Akan tetapi, akhir-akhir ini semakin marak muncul akun atau konten di dalam social media yang berisi kegiatan atau konten asusila dan aktifitas yang tidak senonoh. Hal ini memang salah satu dampak negatif dari social media yang perlu disikapi dengan bijaksana dan usaha bersama untuk menghilangkan atau meminimalkan keberadaan akun atau konten negatif.
          Oleh karena itu, seyogyanya masyarakat turut serta mamantau dan ikut menanggulangi keberadaan akun atau konten negatif di dalam social media tersebut melalui metoda yang telah disediakan pada setiap situs atau aplikasi social media, yaitu dengan cara memberi flag atau menandai akun atau konten yang bersifat asusila dan tidak senonoh.</p>

             <p style="text-align: justify;">Beberapa cara melakukan flag atau menandai, adalah :</p>

            <p style="text-align: justify;">Pada twitter, dapat memberi flag atau menandai dengan cara dari Tweet, klik atau tekan “more” (tanda “•••” di web atau iOS, Android). Kemudian Pilih “Report”, selanjutnya pilih “It displays a sensitive [image/video/media]”</p>

            <p style="text-align: justify;">Pada Facebook, lakukan flag atau menandai dengan cara tekan tanda “•••” kemudian pilih “Report Page”, selanjutnya pilih “I think it shouldn’t be on Facebook”, kemudian pilih “It’s sexually explicit” kemudian klik “Submit to Facebook for Review”.</p>

             <p style="text-align: justify;">Pada YouTube, lakukan flag atau menandai dengan cara tekan tanda “•••” atau “more action” kemudian pilih “sexual content” kemudian pilih sub kriterianya antara lain: graphic sexual activity, nudity, suggestive but without nudity, content involving minnors, dan other sexual content. Selanjutnya jika diperlukan dapat menambahkan waktu (timestamp) serta penjelasan tambahan terhadap aktifitas negatif yang kita temukan.
            Peran serta masyarakat ini akan banyak membantu dalam ikut serta menciptakan kondisi interaksi secara sehat pada social media dan menjaga generasi muda untuk tetap teguh menjunjung tinggi moral dan etika bersama.</p>

