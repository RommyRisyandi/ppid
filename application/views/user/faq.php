KETERBUKAAN INFORMASI PUBLIK</h2>
<h4 class="text-primary text-center mb-3">Apakah ada batasan minimal penggunaan APBN/D, sumbangan masyarakat, atau sumber luar negeri untuk dapat dikategorikan sebagai Badan publik ?
          </h4>
          <p style="text-align: justify;">UU KIP tidak membatasi berapa besar dana yang diterima dari APBN/D, sumbangan masyarakat, atau sumber luar negeri untuk dikategorikan sebagai badan publik. Artinya meskipun dana yang diterima tersebut kecil, prinsipnya lembaga tersebut harus terbuka kepada masyarakat…..</p>
          <h4 class="text-primary text-center mb-3">Dimana dan kapan informasi publik dapat diperoleh?</h4>
          <p style="text-align: justify;">Informasi publik dapat diperoleh masing-masing badan publik yang menguasai informasi yang diminta. Untuk informasi yang tersedia setiap saat, badan publik wajib memberikan respon maksimal 17 hari kerja setelah permintaan informasi diterima. Untuk informasi yang diumumkan secara berkala, badan publik wajib memberikan respon pada saat permintaan dilakukan. Sedangkan untuk informasi serta merta wajib diberikan secara cepat tanpa penundaan dan permintaan.</p>
          <h4 class="text-primary text-center mb-3">Apa yang dimaksud cepat, tepat waktu, biaya ringan, dan cara sederhana?</h4>
          <p style="text-align: justify;">Cepat artinya penyampaian informasi publik dilakukan secepat mungkin tanpa penundaan. Tepat waktu artinya penyampaian informasi publik tidak melebihi jangka waktu yang telah ditetapkan oleh UU KIP.Biaya ringan artinya biaya yang dibebankan kepada pemohon adalah biaya yang wajar sesuai dengan harga umum. Misalnya: biaya foto copy pada umumnya 200 per lembar maka badan publik dapat mengenakan biaya standar tersebut. Cara sederhana artinya prosedur untuk mendapatkan informasi harus mudah, tidak menyulitkan, ada kejelasan yang bertanggungjawab terhadap pelayanan permohonan informasi publik, tempat pelayanan mudah informasi publik yang diberikan dalam bahasa yang mudah dipahami, dan memberikan kemudahan kepada setiap pemohon informasi termasuk mereka yang memilik kemampuan berbeda.Untuk menerapkan asas tersebut, maka badan publik juga harus memiliki SOP (Standard Operational Procedure).</p>
          <h4 class="text-primary text-center mb-3">Bagaimana memperoleh informasi publik Kementerian Kominfo?</h4>
          <p style="text-align: justify;">Dengan menghubungi Pejabat Pengelola Informasi dan Dokumentasi (PPID), Kementerian Komunikasi dan Informatika, Jl. Medan Merdeka Barat No. 9, Jakarta 10110, Telp. 021- 3452841 dan email : pelayanan@depkominfo.go.id;</p>
          <h4 class="text-primary text-center mb-3">Apa syarat untuk mendapatkannya?</h4>
          <p style="text-align: justify;">Cara mendapatkannya datang langsung atau via media (telp. fax, email dan website), setelah sebelumnya mengisi formulir permintaan informasi publik yang tersedia dengan melampirkan fotocopy Kartu Tanda Penduduk (KTP) yang masih berlaku dan persyaratan lainnya yang diperlukan;</p>
          <h4 class="text-primary text-center mb-3">Berapa lama informasi publik dapat diperoleh?</h4>
          <p style="text-align: justify;Informasi publik dapat diperoleh paling lambat 17 (tujuh belas) hari kerja;"></p>
           <h4 class="text-primary text-center mb-3">Jam berapa setiap harinya memberikan layanan informasinya?</h4>
           <p style="text-align: justify;">Jam layanan informasi dilaksanakan setiap hari kerja yaitu Senin sampai dengan Jumat dari dari 09.00 WIB s/d 15.00 WIB dengan waktu istirahat jam 12.00 – 13.00 WIB;</p>
           <h4 class="text-primary text-center mb-3">Apakah Informasi Publik yang diminta dikenakan biaya atau tarif ?</h4>
           <p style="text-align: justify;">Informasi yang tersedia semuanya gratis. Jika pemohon informasi memerlukan salinan dokumen yang jumlahnya cukup banyak (tebal) pemohon informasi dapat melakukan penggandaan dengan biaya sendiri;</p>
